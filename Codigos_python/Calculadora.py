n1 = float(input("Digite aqui seu número:"))
n2 = float(input("Digite aqui o outro número:"))
operacao = input("Digite aqui a operação +/-/*//:")

if operacao == "+":
    print(n1 + n2)
elif operacao == "-":
    print(n1 - n2)
elif operacao == "*":
    print(n1 * n2)
elif operacao == "/":
    print(n1 / n2)