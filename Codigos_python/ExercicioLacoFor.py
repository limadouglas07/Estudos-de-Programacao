# Laço For
animais =[ 'gato','cachorro','leão','camelo']
for a in animais:
    print(a,"string de tamanho", len(a))
        
sequencia = [0,1,2,3,4,5]
for num in sequencia:
    print(num)
    
for i in range(10):
    print(i, end=" ")

for i in range (3,8):
    print(i,end=" ")
    
for i in range (0,21,2):
    print(i, end =" ")